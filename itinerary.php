The beauty of Zomba plateau and the rich history the city offers do not conclude everything, the journey to explore with Kwenda explorers continues. After having your breakfast on the 3rd day in the country at Kuchawe, Liwonde National Park is next to pick from the travel basket. The National Park is located on the banks of Shire River and Lake Malombe which flows from Lake Malawi. 
